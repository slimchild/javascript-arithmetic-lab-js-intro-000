function add(a,b){
  return a + b;

}

function subtract(b,a){
  return b - a;

}

function multiply(a,b){
  return a * b;

}

function divide(a,b){
  return a / b;

}

function inc(n){
  return ++n;

}

function dec(n){
  return --n;

}

function makeInt(n){
  parseInt('n', 10)
  return n;

}

function preserveDecimal(n){
  return parseFloat('n');
}
